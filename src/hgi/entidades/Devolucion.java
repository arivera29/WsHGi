package hgi.entidades;

public class Devolucion {
	private String numeroExpediente;
	private String numeroGuia;
	private String numeroOrden;
	private String codigoMensajero;
	private String fechaDevolucion;
	private String causalDevolucion;
	private String latitud;
	private String longitud;
	private String token;
	public String getNumeroExpediente() {
		return numeroExpediente;
	}
	public void setNumeroExpediente(String numeroExpediente) {
		this.numeroExpediente = numeroExpediente;
	}
	public String getNumeroGuia() {
		return numeroGuia;
	}
	public void setNumeroGuia(String numeroGuia) {
		this.numeroGuia = numeroGuia;
	}
	public String getNumeroOrden() {
		return numeroOrden;
	}
	public void setNumeroOrden(String numeroOrden) {
		this.numeroOrden = numeroOrden;
	}
	public String getCodigoMensajero() {
		return codigoMensajero;
	}
	public void setCodigoMensajero(String codigoMensajero) {
		this.codigoMensajero = codigoMensajero;
	}
	public String getFechaDevolucion() {
		return fechaDevolucion;
	}
	public void setFechaDevolucion(String fechaDevolucion) {
		this.fechaDevolucion = fechaDevolucion;
	}
	public String getCausalDevolucion() {
		return causalDevolucion;
	}
	public void setCausalDevolucion(String causalDevolucion) {
		this.causalDevolucion = causalDevolucion;
	}
	public String getLatitud() {
		return latitud;
	}
	public void setLatitud(String latitud) {
		this.latitud = latitud;
	}
	public String getLongitud() {
		return longitud;
	}
	public void setLongitud(String longitud) {
		this.longitud = longitud;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	
	

}
